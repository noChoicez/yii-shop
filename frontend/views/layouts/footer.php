document.onscroll = function()
{
	var menu = document.getElementById("main_navigation_wrap")
	var scroll = getScroll()
	if(parseInt(scroll[1]) >= 180 && scroll[0] >=768){
		add_class(menu,'sticky')
	}else{
		document.getElementById("header").setAttribute("style","padding-bottom:0px;")
		remove_class(menu, 'sticky')
	}	
}

function getScroll(){    
    var scrollTop = 0;    
    if(document.documentElement&&document.documentElement.scrollTop){    
        scrollTop = document.documentElement.scrollTop; 
        scrollWidth = document.documentElement.scrollWidth;  
    }else if(document.body){    
    	scrollWidth = document.body.scrollWidth
        scrollTop = document.body.scrollTop;    
    }    
    return [scrollWidth,scrollTop];    
}

   
